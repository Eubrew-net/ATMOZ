function y=monitor_brewer_c(brewer_c)
brewer_c=brewer_c(:);
% x brewer counts/second

%power 
%       p(1) =      -0.307 ;  p(2) =        2871 ;  p(3) =  -5.493e+06 ;  p(4) =   3.227e+09 ; 
%       q(1) =           1 ;  q(2) =       -1570 ; q(3) =   3.006e+05 ;  q(4) =   3.639e+08 ; 

  % 
       p(1) =        2.56 ; % (2.402, 2.718)
       p(2) =       -1576 ; % (-3125, -25.66)
       p(3) =   -4.74e+06 ; % (-7.857e+06, -1.622e+06)
       p(4) =    4.33e+09 ; % (2.477e+09, 6.183e+09)
       q(1) =       -1563 ; % (-1754, -1372)
       q(2) =   2.863e+05 ; % (-1.173e+05, 6.9e+05)
       q(3) =   3.719e+08 ; % (1.31e+08, 6.129e+08)
  
  p2=[ 0.000000111437878  -0.004965497078077  55.759874585561093];
  
  %
  %p9=[-0.0789    0.6307   -1.7155    1.3779    1.3373   -1.9403   -0.5305    0.3071   -0.3543   -3.7328];
p9=[   -0.078948137740667   0.630710344804239  -1.715475790715776   1.377916786588999  ...
       1.337294007199893  -1.940254240302504  -0.530503457692454   0.307135957762662  ...
       -0.354316051518479  -0.842464713624936
       ];


  %p9=[-0.078948137740669   0.630710344804248  -1.715475790715780   1.377916786588986   1.337294007199899  -1.940254240302490  -0.530503457692456   0.307135957762657 -0.354316051518479  -3.732836471521101];   
  mu=1E5*[ 2.150462855260889
           2.179588347551255
          ];    
       
for i=1:size(brewer_c,1)
    x=brewer_c(i);
    if x<=0
        r=1000;
    elseif x>0 &  x<=10^4
       %r=exp((polyval(p,x)/polyval(q,x))); 
       r=exp((p(1)*x^3 + p(2)*x^2 + p(3)*x + p(4))/(x^3 + q(1)*x^2 + q(2)*x + q(3)));
    elseif  x>10000  &  x<20000
        r=polyval(p2,x);
    else                
        r=exp(polyval(p9,x,[],mu));
    end
    y(i)=r;
end
     